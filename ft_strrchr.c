#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	while (s[i])
		i++;
	while (i > 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
